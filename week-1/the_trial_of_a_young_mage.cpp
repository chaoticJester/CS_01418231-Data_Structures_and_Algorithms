int main() {
    //สร้างตัวแปรและรับจำนวนคาถาที่ทางโรงเรียนอนุญาตให้ผู้เข้าสอบใช้ จำนวนมานาที่มิเอนะมี
    //และจำนวนคาถาที่มิเอนะ
    int spell_limit;
    int miena_mana;
    int spell_known;

    cin >> spell_limit >> miena_mana;
    cin >> spell_known;
    cin.ignore();

    //สร้าง Array ของ Spell
    int spells_cost[spell_known];
    float spells_point[spell_known];
    string line;
    
    //ลูปรับคาถาที่มิเอนะใช้ได้
    for(int i = 0; i < spell_known; i++) {
        
        getline(cin, line);

        char type = line[0];
        char dmp = line[2];
        int level = dmp - '0';
        int last_space = line.rfind(' ');
        spells_cost[i] = stoi(line.substr(last_space + 1));

        int score;
        float modifier;

        if(type == 'F' || type == 'W') {
            score = 12;
        } else if(type == 'A') {
            score = 15;
        } else if(type == 'E') {
            score = 10;
        } else if(type == 'L' || type == 'D') {
            score = 20;
        } 

        if(level >= 1 && level <= 5) {
            modifier = 0.1 * level;
        } else if(level == 6 || level == 7) {
            modifier = 0.5 + (0.15 * (level - 5));
        } else {
            modifier = 1.0;
        }
        
        spells_point[i] = score * modifier;
    }

    cout << floor(calMaxScore(spells_cost, spells_point, spell_limit, miena_mana, spell_known)) << endl;
    return 0; 
}




